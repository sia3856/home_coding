void use_hpotion(double *hp, double *m_hp, int *potion_count, int amount, const char *type )
{
    if((*hp) + amount > *m_hp && *potion_count > 0)
    {
        system("clear");
        *hp = *m_hp;
        enter(10);
        printf("              %s + %.1lf\n", type, *m_hp - *hp);
        (*potion_count)--;
        sleep(1);
        
    }
    else if(*hp < *m_hp && *potion_count > 0)
    {
        system("clear");
        *hp += amount;
        enter(10);
        printf("              %s + %d\n", type, amount);
        (*potion_count)--;
        sleep(1);
        
    }
}

